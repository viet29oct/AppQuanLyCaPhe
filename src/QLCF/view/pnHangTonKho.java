    private void loadTableDanhSach(){
        DefaultTableModel model = (DefaultTableModel) tblDanhSach.getModel();
        model.setRowCount(0);
        
        NhaCungCapDAO dao = new NhaCungCapDAO();
        List<NhaCungCap> list = dao.getAll();
        for(NhaCungCap ncc : list){
            model.addRow(new Object[]{
                ncc.getMaNCC(),
                ncc.getTenNCC(),
                ncc.getLoaiHang()
            });
        }
    }

    private void loadTableMatHangNCC(int maNCC){
        DefaultTableModel model = (DefaultTableModel) tblTonKho.getModel();
        model.setRowCount(0);
        
        NhaCungCapDAO dao = new NhaCungCapDAO();
        List<MatHang> list = dao.getMatHangByNCC(maNCC);
        for(MatHang mh : list){
            model.addRow(new Object[]{
                mh.getMaHang(),
                mh.getTenHang(),
                mh.getDonVi(),
                mh.getGiaNhap(),
                mh.getSoLuongTon()
            });
        }
    }
