    private void btnXemChiTietActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXemChiTietActionPerformed
        if(selectedMaNCC == null){
            JOptionPane.showMessageDialog(this, "Vui long chon mot nha cung cap");
            return;
        }
        
        int ma = Integer.parseInt(selectedMaNCC);
        
        NhaCungCapDAO dao = new NhaCungCapDAO();
        NhaCungCap ncc = dao.getNCCByID(ma);
        
        txtMaNCC.setText(String.valueOf(ncc.getMaNCC()));
        txtTenNCC.setText(ncc.getTenNCC());
        txtLoaiHang.setText(ncc.getLoaiHang());
        txtLienHe.setText(ncc.getLienHe());
        txtSDT.setText(ncc.getSoDienThoai());
        txtEmail.setText(ncc.getEmail());
        txtDiaChi.setText(ncc.getDiaChi());
        
        loadTableMatHangNCC(ma);
    }//GEN-LAST:event_btnXemChiTietActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed

        int rowMatHang = tblTonKho.getSelectedRow();
        if(rowMatHang == -1){
            JOptionPane.showMessageDialog(this, "Chọn một hàng từ bảng!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }else{
            
        try{
            String soLuongMoi = txtTon.getText().trim();
            
            if(soLuongMoi.isEmpty()){
                JOptionPane.showMessageDialog(this, "Nhập số lượng tồn mới!", "Thông báo", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            int tonMoi = Integer.parseInt(soLuongMoi);
            
            if(tonMoi < 0){
                JOptionPane.showMessageDialog(this, "Số lượng tồn không được âm!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            tblTonKho.setValueAt(tonMoi, rowMatHang, 4);
            
            txtTon.setText(String.valueOf(tonMoi));
                        
            JOptionPane.showMessageDialog(this, "Đã thay đổi số lượng tồn!", "Thành công", JOptionPane.INFORMATION_MESSAGE);
        }catch(Exception e){
            e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try{
            if(tblTonKho.getRowCount() == 0){
                JOptionPane.showMessageDialog(this, "Không có dữ liệu cập nhật!", "Thông báo", JOptionPane.WARNING_MESSAGE);
                return;
            }
            int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn lưu tất cả thay đổi xuống database?", "Xác nhận cập nhật", JOptionPane.YES_NO_OPTION);
            
            if(confirm == JOptionPane.YES_OPTION){
                NhaCungCapDAO dao = new NhaCungCapDAO();
                boolean hasChanges = false;
                boolean allsuccess = true;
                
                for(int row = 0; row < tblTonKho.getRowCount(); row++){
                    int maHang = Integer.parseInt(tblTonKho.getValueAt(row, 0).toString());
                    int soLuongTon = Integer.parseInt(tblTonKho.getValueAt(row, 4).toString());
                    
                    boolean success = dao.updateSoLuongTon(maHang, soLuongTon);
                    
                    if(success){
                        hasChanges = true;
                    }else{
                        allsuccess = false;
                        JOptionPane.showMessageDialog(this, "Cập nhật thất bại cho mã hàng: " + maHang, "Lỗi", JOptionPane.ERROR_MESSAGE);
                    }
                }
                if(hasChanges && allsuccess){
                    JOptionPane.showMessageDialog(this, "Cập nhật tất cả số lượng tồn kho thành công!", "Thành công", JOptionPane.INFORMATION_MESSAGE);
                }else if(hasChanges && !allsuccess){
                    JOptionPane.showMessageDialog(this, 
                        "Một số mặt hàng cập nhật thất bại!", 
                        "Cảnh báo", JOptionPane.WARNING_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(this, 
                        "Không có thay đổi nào được cập nhật!", 
                        "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }catch(Exception e){
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btnUpdateActionPerformed


