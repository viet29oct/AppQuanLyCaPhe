    private void loadTable() {
        List<NhaCungCap> list = nccdao.getAll();
        model = (DefaultTableModel) tblNhaCungCap.getModel();
        model.setRowCount(0);
        
        for (NhaCungCap ncc : list) {
            model.addRow(new Object[]{
                ncc.getMaNCC(),
                ncc.getTenNCC(),
                ncc.getLoaiHang(),
                ncc.getLienHe(),
                ncc.getSoDienThoai(),
                ncc.getEmail(),
                ncc.getDiaChi()
            });
        }
    }
    
