    private void tblNhaCungCapMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhaCungCapMouseClicked
        int selectedRow = tblNhaCungCap.getSelectedRow();
        if (selectedRow != -1) {
            txtTenNCC.setText(tblNhaCungCap.getValueAt(selectedRow, 1).toString());
            
            Object loaiHang = tblNhaCungCap.getValueAt(selectedRow, 2);
            txtLoaiHang.setText(loaiHang != null ? loaiHang.toString() : "");
            
            Object lienHe = tblNhaCungCap.getValueAt(selectedRow, 3);
            txtLienHe.setText(lienHe != null ? lienHe.toString() : "");
            
            Object sdt = tblNhaCungCap.getValueAt(selectedRow, 4);
            txtSDT.setText(sdt != null ? sdt.toString() : "");
            
            Object email = tblNhaCungCap.getValueAt(selectedRow, 5);
            txtEmail.setText(email != null ? email.toString() : "");
            
            Object diaChi = tblNhaCungCap.getValueAt(selectedRow, 6);
            txtDiaChi.setText(diaChi != null ? diaChi.toString() : "");
        }
    }//GEN-LAST:event_tblNhaCungCapMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String tenNCC = txtTenNCC.getText().trim();
        String loaiHang = txtLoaiHang.getText().trim();
        String lienHe = txtLienHe.getText().trim();
        String sdt = txtSDT.getText().trim();
        String email = txtEmail.getText().trim();
        String diaChi = txtDiaChi.getText().trim();
        
        if (tenNCC.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên nhà cung cấp!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        NhaCungCap ncc = new NhaCungCap();
        ncc.setTenNCC(tenNCC);
        ncc.setLoaiHang(loaiHang);
        ncc.setLienHe(lienHe);
        ncc.setSoDienThoai(sdt);
        ncc.setEmail(email);
        ncc.setDiaChi(diaChi);
        
        if (nccdao.add(ncc)) {
            JOptionPane.showMessageDialog(this, "Thêm nhà cung cấp thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            loadTable();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int selectedRow = tblNhaCungCap.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nhà cung cấp cần sửa!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int maNCC = (int) tblNhaCungCap.getValueAt(selectedRow, 0);
        String tenNCC = txtTenNCC.getText().trim();
        String loaiHang = txtLoaiHang.getText().trim();
        String lienHe = txtLienHe.getText().trim();
        String sdt = txtSDT.getText().trim();
        String email = txtEmail.getText().trim();
        String diaChi = txtDiaChi.getText().trim();
        
        if (tenNCC.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên nhà cung cấp!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        NhaCungCap ncc = new NhaCungCap();
        ncc.setMaNCC(maNCC);
        ncc.setTenNCC(tenNCC);
        ncc.setLoaiHang(loaiHang);
        ncc.setLienHe(lienHe);
        ncc.setSoDienThoai(sdt);
        ncc.setEmail(email);
        ncc.setDiaChi(diaChi);
        
        if (nccdao.update(ncc)) {
            JOptionPane.showMessageDialog(this, "Cập nhật thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            loadTable();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int selectedRow = tblNhaCungCap.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nhà cung cấp cần xóa!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa nhà cung cấp này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) return;
        
        int maNCC = (int) tblNhaCungCap.getValueAt(selectedRow, 0);
        
        if (nccdao.delete(maNCC)) {
            JOptionPane.showMessageDialog(this, "Xóa thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            loadTable();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Xóa thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
       String keyword = txtfind.getText().trim();
        
        if (keyword.isEmpty()) {
            loadTable();
            return;
        }

        List<NhaCungCap> list = nccdao.findByName(keyword);
        model = (DefaultTableModel) tblNhaCungCap.getModel();
        model.setRowCount(0);

        for (NhaCungCap ncc : list) {
            model.addRow(new Object[]{
                ncc.getMaNCC(),
                ncc.getTenNCC(),
                ncc.getLoaiHang(),
                ncc.getLienHe(),
                ncc.getSoDienThoai(),
                ncc.getEmail(),
                ncc.getDiaChi()
            });
        }

        if (list.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy nhân viên nào!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed


