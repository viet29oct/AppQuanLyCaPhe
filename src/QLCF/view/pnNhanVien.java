    private void BtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddActionPerformed
        try {
            String role = "nhanvien";
            String username = txtusername.getText().trim();
            String password = new String(txtpassword.getPassword()).trim();
            String ten = txtfullname.getText().trim();
            String sdt = txtphone.getText().trim();
            String gioiTinh = Cbgioitinh.getSelectedItem().toString();
            java.util.Date utilDate = txtbirthday.getDate();

            if (username.isEmpty() || password.isEmpty() || ten.isEmpty() || sdt.isEmpty() || gioiTinh.equalsIgnoreCase("Select") || utilDate == null) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!","Cảnh báo", JOptionPane.WARNING_MESSAGE);
                return;
            }

            TaiKhoanDao tkDao = new TaiKhoanDao();
            NhanVienDao nvDao = new NhanVienDao();

            if (tkDao.checkRegister(username)) {
                JOptionPane.showMessageDialog(this, "Tài khoản này đã tồn tại!",
                        "Cảnh báo", JOptionPane.WARNING_MESSAGE);
                return;
            }

            TaiKhoan tkMoi = new TaiKhoan(username, password, role);
            tkDao.addTaiKhoan(tkMoi);

            int taiKhoanId = getIdByUsername(username);
            if (taiKhoanId <= 0) {
                JOptionPane.showMessageDialog(this, "Không thể lấy ID tài khoản!","Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }

            java.sql.Date ngaySinh = new java.sql.Date(utilDate.getTime());
            NhanVien nv = new NhanVien();
            nv.setTen(ten);
            nv.setSdt(sdt);
            nv.setGioiTinh(gioiTinh);
            nv.setNgaySinh(ngaySinh);
            nv.setTaikhoanId(taiKhoanId);
            nv.setRole(role);

            
            nvDao.addNhanVien(nv);
            JOptionPane.showMessageDialog(this, "Thêm nhân viên thành công!","Hệ thống", JOptionPane.INFORMATION_MESSAGE);

            loadTableData();
            clearForm();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi thêm nhân viên: " + e.getMessage(),
                    "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BtnAddActionPerformed

    private void BtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnUpdateActionPerformed
        int selectedRow = tblNhanVien.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên cần cập nhật!", 
                    "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            int taikhoan_id = (int) tblNhanVien.getValueAt(selectedRow,5);
            int id = (int) tblNhanVien.getValueAt(selectedRow, 0);
            String ten = txtfullname.getText().trim();
            String gioiTinh = Cbgioitinh.getSelectedItem().toString();
            String sdt = txtphone.getText().trim();
            String username = txtusername.getText().trim();
            String password = new String(txtpassword.getPassword());
            java.util.Date utilDate = txtbirthday.getDate();

            if (ten.isEmpty() || sdt.isEmpty() || gioiTinh.equalsIgnoreCase("Select") || utilDate == null) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
                return;
            }

            java.sql.Date ngaySinh = new java.sql.Date(utilDate.getTime());
            NhanVien nv = new NhanVien();
            TaiKhoan tk = new TaiKhoan();
            
            tk.setId(taikhoan_id);
            tk.setUsername(username);
            tk.setPassword(password);
            tk.setRole("Nhan vien");

            nv.setId(id);
            nv.setTen(ten);
            nv.setGioiTinh(gioiTinh);
            nv.setSdt(sdt);
            nv.setNgaySinh(ngaySinh);
            
            NhanVienDao nvdao = new NhanVienDao();
            TaiKhoanDao tkdao = new TaiKhoanDao();
            nvdao.updateNhanVien(nv);
            tkdao.updateTaiKhoan(tk);
            
            JOptionPane.showMessageDialog(this, "Cập nhật nhân viên thành công!", "Hệ thống", JOptionPane.INFORMATION_MESSAGE);
            loadTableData();
            clearForm();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BtnUpdateActionPerformed

    private void BtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDeleteActionPerformed
        int selectedRow = tblNhanVien.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên cần xóa!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, 
                "Bạn có chắc muốn xóa nhân viên này?", 
                "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) return;

        try {
            int id = (int) tblNhanVien.getValueAt(selectedRow, 0);
            NhanVienDao nvdao = new NhanVienDao();
            TaiKhoanDao tkdao = new TaiKhoanDao();
            tkdao.deleteTaiKhoan(id);
            nvdao.deleteNhanVien(id);
            
            JOptionPane.showMessageDialog(pnNhanVien.this, "Xóa nhân viên thành công!", "Hệ thống", JOptionPane.INFORMATION_MESSAGE);
            loadTableData();
            clearForm();
            
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi xóa: " + e.getMessage(), 
                    "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BtnDeleteActionPerformed

    private void BtnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnFindActionPerformed
        String keyword = txtfind.getText().trim();
        
        if (keyword.isEmpty()) {
            loadTableData();
            return;
        }

        List<NhanVien> list = nvdao.findByName(keyword);
        model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);

        for (NhanVien nv : list) {
            model.addRow(new Object[]{
                nv.getId(),
                nv.getTen(),
                nv.getGioiTinh(),
                nv.getSdt(),
                nv.getNgaySinh(),
                nv.getTaikhoanId(),
                nv.getRole()
            });
        }

        if (list.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy nhân viên nào!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_BtnFindActionPerformed

    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        int selectedRow = tblNhanVien.getSelectedRow();
        if (selectedRow != -1) {
            txtfullname.setText(tblNhanVien.getValueAt(selectedRow, 1).toString());
            Object gioiTinh = tblNhanVien.getValueAt(selectedRow, 2);
            if (gioiTinh != null) {
                String gt = gioiTinh.toString();
                if (gt.equalsIgnoreCase("Nam")) {
                    Cbgioitinh.setSelectedIndex(1);
                } else if (gt.equalsIgnoreCase("Nu")) {
                    Cbgioitinh.setSelectedIndex(2);
                } else {
                    Cbgioitinh.setSelectedIndex(0);
                }
            }
            
            Object sdt = tblNhanVien.getValueAt(selectedRow, 3);
            txtphone.setText(sdt != null ? sdt.toString() : "");
            
            Object ngaySinh = tblNhanVien.getValueAt(selectedRow, 4);
            if (ngaySinh != null && ngaySinh instanceof java.util.Date) {
                txtbirthday.setDate((java.util.Date) ngaySinh);
            } else {
                txtbirthday.setDate(null);
            }

            txtusername.setText("");
            txtpassword.setText("");
        }
    }//GEN-LAST:event_tblNhanVienMouseClicked



