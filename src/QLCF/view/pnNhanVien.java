    private int getIdByUsername(String username) {
        int id = -1;
        String sql = "SELECT id FROM taikhoan WHERE username = ?";
        java.sql.Connection conn = null;
        java.sql.PreparedStatement pre = null;
        java.sql.ResultSet rs = null;
        
        try {
            conn = QLCF.connection.connectDB.getInstance();
            pre = conn.prepareStatement(sql);
            pre.setString(1, username);
            rs = pre.executeQuery();
            
            if (rs.next()) {
                id = rs.getInt("id");
                System.out.println("ID tài khoản tìm thấy: " + id);
            } else {
                System.out.println("Không tìm thấy username: " + username);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("Lỗi getIdByUsername: " + e.getMessage());
        } finally {
            try {
                if (rs != null) rs.close();
                if (pre != null) pre.close();
                if (conn != null) QLCF.connection.connectDB.closeConnect(conn);
            } catch (Exception ignored) {}
        }
        return id;
    }


